<?php

namespace JCSGYK\AdminBundle\Entity;

use Doctrine\ORM\Mapping as ORM;
use Symfony\Component\Validator\Constraints as Assert;
use Symfony\Component\HttpFoundation\File\UploadedFile;

/**
 * DocTemplate
 *
 * @ORM\Table(name="doc_template")
 * @ORM\Entity(repositoryClass="JCSGYK\AdminBundle\Entity\DocTemplateRepository")
 */
class DocTemplate
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer", nullable=false)
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="IDENTITY")
     */
    private $id;

    /**
     * @var integer
     *
     * @ORM\Column(name="company_id", type="integer", nullable=true)
     */
    private $companyId;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=255, nullable=true)
     */
    private $name;

    /**
     * @var string
     *
     * @ORM\Column(name="original_name", type="string", length=255, nullable=true)
     */
    private $originalName;

    /**
     * @var string
     *
     * @ORM\Column(name="mime_type", type="string", length=255, nullable=true)
     */
    private $mimeType;

    /**
     * @var string
     *
     * @ORM\Column(name="file", type="blob", nullable=true)
     */
    private $file;

    /**
     * @var boolean
     *
     * @ORM\Column(name="is_active", type="boolean", nullable=true)
     */
    private $isActive;

    /**
     * @var boolean
     *
     * @ORM\Column(name="client_template", type="boolean", nullable=true)
     */
    private $clientTemplate;

    /**
     * @var boolean
     *
     * @ORM\Column(name="problem_template", type="boolean", nullable=true)
     */
    private $problemTemplate;

    /**
     * @var boolean
     *
     * @ORM\Column(name="client_type", type="integer", nullable=true)
     */
    private $clientType;

    /**
     * @Assert\File(maxSize="8000000")
     */
    private $upload;

    /**
     * @var Club
     *
     * @ORM\ManyToOne(targetEntity="Club", inversedBy="clientcaterings")
     * @ORM\JoinColumn(name="club_id", referencedColumnName="id")
     */
    private $club;

    /**
     * Get the list of fields for change tracking
     * @return array of field names
     */
    public function getHistoryFields()
    {
        return ['name', 'originalName', 'mimeType', 'isActive', 'docType', 'clientType'];
    }

    /**
     * Returns the required information for the entity history
     * Usage: $this->container->get('jcs.ds')->getinfo($entity);
     * @return array
     */
    public function getHistoryInfo()
    {
        return [
            'default' => [
                'hash' => 'DocTemplate',
                'id'   => $this->getId(),
                'data' => null
            ],

            // the final hash will be generated by the DataStore::getHistoryInfo() function
        ];
    }


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set companyId
     *
     * @param integer $companyId
     *
     * @return DocTemplate
     */
    public function setCompanyId($companyId)
    {
        $this->companyId = $companyId;

        return $this;
    }

    /**
     * Get companyId
     *
     * @return integer
     */
    public function getCompanyId()
    {
        return $this->companyId;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return DocTemplate
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set originalName
     *
     * @param string $originalName
     *
     * @return DocTemplate
     */
    public function setOriginalName($originalName)
    {
        $this->originalName = $originalName;

        return $this;
    }

    /**
     * Get originalName
     *
     * @return string
     */
    public function getOriginalName()
    {
        return $this->originalName;
    }

    /**
     * Set mimeType
     *
     * @param string $mimeType
     *
     * @return DocTemplate
     */
    public function setMimeType($mimeType)
    {
        $this->mimeType = $mimeType;

        return $this;
    }

    /**
     * Get mimeType
     *
     * @return string
     */
    public function getMimeType()
    {
        return $this->mimeType;
    }

    /**
     * Set file
     *
     * @param string $file
     *
     * @return DocTemplate
     */
    public function setFile($file)
    {
        $this->file = $file;

        return $this;
    }

    /**
     * Get file
     *
     * @return string
     */
    public function getFile()
    {
        return $this->file;
    }

    /**
     * Set isActive
     *
     * @param boolean $isActive
     *
     * @return DocTemplate
     */
    public function setIsActive($isActive)
    {
        $this->isActive = $isActive;

        return $this;
    }

    /**
     * Get isActive
     *
     * @return boolean
     */
    public function getIsActive()
    {
        return $this->isActive;
    }

    /**
     * Set clientType
     *
     * @param integer $clientType
     *
     * @return DocTemplate
     */
    public function setClientType($clientType)
    {
        $this->clientType = $clientType;

        return $this;
    }

    /**
     * Get clientType
     *
     * @return integer
     */
    public function getClientType()
    {
        return $this->clientType;
    }

    /**
     * Sets uploaded file.
     *
     * @param UploadedFile $file
     */
    public function setUpload(UploadedFile $file = null)
    {
        $this->upload = $file;
        if (!is_null($file)) {
            $filename = $file->getRealPath();
            if (file_exists($filename)) {
                $this->setOriginalName($file->getClientOriginalName());
                // mime type
                $finfo = new \finfo(FILEINFO_MIME);
                $this->setMimeType($finfo->file($filename));
                $this->setFile(file_get_contents($filename));
            }
        }
    }

    /**
     * Get file.
     *
     * @return UploadedFile
     */
    public function getUpload()
    {
        return $this->upload;
    }

    /**
     * Set clientTemplate
     *
     * @param boolean $clientTemplate
     *
     * @return DocTemplate
     */
    public function setClientTemplate($clientTemplate)
    {
        $this->clientTemplate = $clientTemplate;

        return $this;
    }

    /**
     * Get clientTemplate
     *
     * @return boolean
     */
    public function getClientTemplate()
    {
        return $this->clientTemplate;
    }

    /**
     * Set problemTemplate
     *
     * @param boolean $problemTemplate
     *
     * @return DocTemplate
     */
    public function setProblemTemplate($problemTemplate)
    {
        $this->problemTemplate = $problemTemplate;

        return $this;
    }

    /**
     * Get problemTemplate
     *
     * @return boolean
     */
    public function getProblemTemplate()
    {
        return $this->problemTemplate;
    }

    /**
     * Set club
     *
     * @param \JCSGYK\AdminBundle\Entity\Club $club
     *
     * @return DocTemplate
     */
    public function setClub(\JCSGYK\AdminBundle\Entity\Club $club = null)
    {
        $this->club = $club;

        return $this;
    }

    /**
     * Get club
     *
     * @return \JCSGYK\AdminBundle\Entity\Club
     */
    public function getClub()
    {
        return $this->club;
    }
}
